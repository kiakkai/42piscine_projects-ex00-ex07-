#include <stdio.h>
char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main(void)
{
	char source[25]="abcd";
	char destination[25];
	//destination = "i";
	printf("%s",  ft_strncpy(destination, source, 5));
	//printf("Destination = %s\n",destination);
	return 0;
}
