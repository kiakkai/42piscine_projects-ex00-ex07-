#include <unistd.h>
#include <stdio.h>

void	ft_print_alphabet(void)
{
	char	character;

	character = 'a';
	while (character <= 'z')
	{
		write(1, &character, 1);
		character++;
	}
}


int	main(void)
{
	ft_print_alphabet();
	int p;
	p = 6;
	write(1, &p, 4);
}

